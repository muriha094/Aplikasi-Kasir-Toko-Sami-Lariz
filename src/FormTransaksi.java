    private void btnHapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHapusActionPerformed
        // TODO add your handling code here:                                     
    try {
        String id = txtIDKonsumen.getText();

        // Pastikan field tidak kosong
        if (id.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Silakan pilih data yang ingin dihapus dari tabel.");
            return;
        }

        int konfirmasi = JOptionPane.showConfirmDialog(
            this,
            "Apakah kamu yakin ingin menghapus data dengan ID: " + id + "?",
            "Konfirmasi Hapus",
            JOptionPane.YES_NO_OPTION
        );

        if (konfirmasi == JOptionPane.YES_OPTION) {
            String sql = "DELETE FROM swalayan WHERE id_konsumen=?";
            Connection conn = Config.configDB();
            PreparedStatement pst = conn.prepareStatement(sql);
            pst.setString(1, id);
            pst.executeUpdate();

            JOptionPane.showMessageDialog(this, "✅ Data berhasil dihapus.");

            load_table();
            kosongkanForm();
        }

    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "❌ Gagal menghapus data:\n" + e.getMessage());
    }

    }//GEN-LAST:event_btnHapusActionPerformed

    private void btnCetakActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCetakActionPerformed
        // TODO add your handling code here:                                   
         try {
                String reportPath = "src/report/lapor_tokolariz.Jasper";

                HashMap<String, Object> parameters = new HashMap<>();
                Connection conn = Config.configDB(); // pastikan ini benar mengarah ke DB yang berisi data

                if (conn == null) {
                    throw new Exception("Koneksi database gagal.");
                }

                JasperPrint print = JasperFillManager.fillReport(reportPath, parameters, conn);

                if (print.getPages().isEmpty()) {
                    JOptionPane.showMessageDialog(null, "Laporan tidak memiliki halaman. Cek isi data atau query-nya.");
                    return;
                }

                JasperViewer viewer = new JasperViewer(print, false);
                viewer.setExtendedState(JasperViewer.MAXIMIZED_BOTH);
                viewer.setVisible(true);
            } catch (Exception e) {
                e.printStackTrace();
                JOptionPane.showMessageDialog(null, "Gagal mencetak laporan: " + e.getMessage());
            }
    }//GEN-LAST:event_btnCetakActionPerformed

