    private void load_table() {
    DefaultTableModel model = new DefaultTableModel();

    model.addColumn("ID Konsumen");
    model.addColumn("Nama Konsumen");
    model.addColumn("Alamat");
    model.addColumn("Nama Barang");
    model.addColumn("Harga");
    model.addColumn("Jumlah");
    model.addColumn("Jumlah Belanja");
    model.addColumn("Diskon");
    model.addColumn("Bonus");
    model.addColumn("Total Bayar");

    try {
        String sql = "SELECT * FROM swalayan";
        Connection conn = (Connection) Config.configDB();
        Statement stm = conn.createStatement();
        ResultSet res = stm.executeQuery(sql);

        while (res.next()) {
            model.addRow(new Object[]{
                res.getString("id_konsumen"),
                res.getString("nama_konsumen"),
                res.getString("alamat"),
                res.getString("nama_barang"),
                res.getInt("harga"),
                res.getInt("jumlah"),
                res.getInt("jumlah_belanja"),
                res.getInt("diskon"),
                res.getString("bonus"),
                res.getInt("total_bayar")
            });
        }

        tblData.setModel(model);
    } catch (SQLException e) {
        JOptionPane.showMessageDialog(null, "❌ Gagal memuat data:\n" + e.getMessage());
    }
}
