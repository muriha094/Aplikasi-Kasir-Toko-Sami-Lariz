    private void btnTambahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTambahActionPerformed
        // TODO add your handling code here:                                        
    // VALIDASI INPUT WAJIB DIISI
    if (txtIDKonsumen.getText().isEmpty() ||
        txtNamaKonsumen.getText().isEmpty() ||
        txtAlamat.getText().isEmpty() ||
        txtNamaBarang.getText().isEmpty() ||
        txtHarga.getText().isEmpty() ||
        txtJumlahBeli.getText().isEmpty()) {

        JOptionPane.showMessageDialog(this, "❗ Semua field wajib diisi.", "Validasi", JOptionPane.WARNING_MESSAGE);
        return;
    }

    // VALIDASI ANGKA UNTUK HARGA & JUMLAH BELI
    int harga, jumlah;
    try {
        harga = Integer.parseInt(txtHarga.getText());
        jumlah = Integer.parseInt(txtJumlahBeli.getText());

        if (harga < 0 || jumlah <= 0) {
            JOptionPane.showMessageDialog(this, "❗ Harga dan jumlah beli harus bernilai positif.", "Validasi", JOptionPane.WARNING_MESSAGE);
            return;
        }
    } catch (NumberFormatException e) {
        JOptionPane.showMessageDialog(this, "❗ Harga dan jumlah beli harus berupa angka.", "Validasi", JOptionPane.WARNING_MESSAGE);
        return;
    }

    // VALIDASI RADIO BUTTON DISKON
    if (!rbDapat.isSelected() && !rbTidak.isSelected()) {
        JOptionPane.showMessageDialog(this, "❗ Pilih apakah dapat diskon atau tidak.", "Validasi", JOptionPane.WARNING_MESSAGE);
        return;
    }

    // HITUNG ARITMATIKA
    int jumlahBelanja = harga * jumlah;
    int diskon = rbDapat.isSelected() ? jumlahBelanja * 10 / 100 : 0;
    String bonus = jumlahBelanja >= 300000 ? "TAS CANTIK" : "VOUCHER";
    int total = jumlahBelanja - diskon;

    // TAMPILKAN KE FIELD OUTPUT
    txtJumlahBelanja.setText(String.valueOf(jumlahBelanja));
    txtDiskon.setText(String.valueOf(diskon));
    txtBonus.setText(bonus);
    txtTotalBayar.setText(String.valueOf(total));

    // SIMPAN KE DATABASE
    try {
        String sql = "INSERT INTO swalayan (id_konsumen, nama_konsumen, alamat, nama_barang, harga, jumlah, jumlah_belanja, diskon, bonus, total_bayar) VALUES "
                + "(?, ?, ?, ?, ?, ?, ?, ?, ?, ?)";
        Connection conn = Config.configDB();
        PreparedStatement pst = conn.prepareStatement(sql);

        pst.setString(1, txtIDKonsumen.getText());
        pst.setString(2, txtNamaKonsumen.getText());
        pst.setString(3, txtAlamat.getText());
        pst.setString(4, txtNamaBarang.getText());
        pst.setInt(5, harga);
        pst.setInt(6, jumlah);
        pst.setInt(7, jumlahBelanja);
        pst.setInt(8, diskon);
        pst.setString(9, bonus);
        pst.setInt(10, total);

        pst.executeUpdate();
        JOptionPane.showMessageDialog(this, "✅ Data berhasil disimpan.");

        load_table();
        kosongkanForm();

    } catch (HeadlessException | SQLException e) {
        JOptionPane.showMessageDialog(this, "❌ Gagal menyimpan data:\n" + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
    }

    }//GEN-LAST:event_btnTambahActionPerformed

